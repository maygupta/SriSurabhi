public class SimpleCryptoException extends Exception {

    private static final long serialVersionUID = 1L;

    public SimpleCryptoException() {
        super();
    }

    public SimpleCryptoException(String message) {
        super(message);
    }

    public SimpleCryptoException(String message, Throwable innerException) {
        super(message, innerException);
    }
}